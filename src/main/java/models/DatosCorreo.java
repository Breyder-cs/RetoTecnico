package models;

public class DatosCorreo {

    private String titulo;
    private String parrafo;
    private String palabraClave;
    private String descripcion;

    public String getTitulo() { return titulo; }

    public void setTitulo(String titulo) { this.titulo = titulo; }

    public String getParrafo() { return parrafo; }

    public void setParrafo(String parrafo) { this.parrafo = parrafo; }

    public String getPalabraClave() { return palabraClave; }

    public void setPalabraClave(String palabraClave) { this.palabraClave = palabraClave; }

    public String getDescripcion() { return descripcion; }

    public void setDescripcion(String descripcion) { this.descripcion = descripcion; }
}
